#include "levelloader.h"

LevelLoader::LevelLoader()
{
    // initialize level container
    std::vector<Level> (this->allLevels);
    // fill it with levels
    this->populateLevels();
}

Level LevelLoader::getLevel(int levelIndex) {
    return this->allLevels.at(levelIndex);
}

